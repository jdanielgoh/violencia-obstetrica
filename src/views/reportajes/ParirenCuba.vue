    <h1>
      Parir en Cuba: cómo un sistema autoritario permite la violencia obstétrica
    </h1>
    <p class="sumario">
      El sistema de atención al parto en Cuba prioriza estadísticas de
      mortalidad infantil, pero ignora indicadores de calidad y prácticas de
      parto humanizado.
    </p>
    <img src="@/assets/img/reportajes/01-ILUSTRACION-WEB.jpg" alt="" />
    <p class="autorxs">
      Por: Partos Rotos, una colaboración de periodistas independientes cubanas.
    </p>
    
    <p>
      El 12 de agosto de 2015, a las dos de la tarde, Paloma López llamó a la
      ambulancia que la trasladaría hasta el Ramón González Coro, un hospital
      ginecobstétrico de La Habana. Había comenzado su trabajo de parto en casa
      desde la mañana porque había escuchado a otras madres decir que en el
      hospital las trataban mal.
    </p>
    <p>
      Llegó con seis centímetros de dilatación, pero sin romper fuente. “Me
      pasaron a la camilla a monitorearme, me levantaron, me llevaron al
      cuartico raro, entonces sin decirme nada (la doctora) saca un pincho y
      ¡pa!, me mete el pincho y me dolió. Y yo con el grito, ¡pero eso qué es! Y
      era para romperme la fuente”.
    </p>
    <p>
      La obstetra se tiró con todo su peso sobre la barriga de Paloma y con el
      antebrazo intentó presionar el útero y empujar la bebé hacia abajo. Del
      susto, Paloma le dio un manotazo a la doctora. Esta, como estaba apoyada
      sobre ella y prácticamente tenía los pies en el aire, cayó al suelo,
      sentada.
    </p>
    <p>
      —¡Mira la puta esta, no quiere que la ayuden! Va a matar a la chiquita
      —cuenta Paloma que gritaba.<br />
      —Doctora, no me diga eso, usted tiene que pedir permiso.<br />
      —No, tú no sabes nada.
    </p>
    <p>
      La doctora intentó varias veces aplicar la maniobra. Paloma reaccionó de
      la misma manera, empujándola, hasta que, conteniendo el dolor, permitió
      que la obstetra se subiera sobre su barriga. “Me halaron, sentí el
      desgarre de mi niña, cómo me la sacaron —cuenta. Ahora sé que eso fue
      antes de tiempo, que no fue orgánico. Y me hizo tremenda rajada la niña
      allá abajo.
    </p>
    <h2>Un problema global y cubano</h2>
    <p>
      En los dos últimos años un número creciente de madres cubanas ha
      compartido sus experiencias de parto en redes sociales y medios
      independientes; lo que ha desatado un #MeToo obstétrico en la isla.
    </p>
    <p>
      Algunas madres han denunciado haberse sentido maltratadas verbal o
      psicológicamente. Otras dijeron que se les negó información de lo que les
      sucedía o nunca se les pidió consentimiento para practicarles ciertas
      intervenciones. Muchas describieron su parto como un evento traumático en
      el que fueron tratadas como seres sin autonomía cuyo bienestar carece de
      importancia.
    </p>
    <p>
      Para algunas, el problema residió en que sufrieron exceso de
      medicalización o prácticas agresivas. Una de ellas es la maniobra de
      Kristeller, que pone presión sobre las costillas y ha sido cuestionada por
      la OMS desde el año 1996. Otra es la episiotomía, un corte en el perineo,
      entre la vagina y el ano, para facilitar el parto y que a menudo se
      realiza sin consentimiento y/o sin necesidad.
    </p>
    <p>Otras pacientes dijeron que se sintieron abandonadas o ignoradas.</p>
    <p>
      Los testimonios han contribuido a visibilizar un problema que existe en la
      mayoría de los países del mundo, pero que en Cuba había permanecido
      especialmente invisibilizado y naturalizado: la violencia obstétrica.
    </p>
    <p>
      La presente investigación, Partos Rotos, pone en evidencia que se trata de
      un problema sistemático en el país. Casi 500 mujeres de todas las
      provincias participaron en el estudio. Ellas llenaron un cuestionario en
      el que se les preguntó cómo fue su parto. En total, se recopiló
      información detallada de 514 nacimientos, por cesárea o parto vaginal, en
      su mayoría ocurridos en las últimas dos décadas.
    </p>
    <p>
      La investigación no se basa en una muestra representativa y sus resultados
      no tienen validez estadística, pero es lo suficientemente amplia como para
      ofrecer un panorama general de cómo se manifiesta la violencia obstétrica
      en el país.
    </p>
    <p>
      Las entrevistadas describieron un sistema de salud en el que sus
      peticiones de tratamiento para el dolor son ignoradas (86%) y en el que
      aún son comunes procedimientos agresivos que en otros países ya no se
      realizan de manera sistemática. En casi la mitad de los partos se practicó
      dilatación manual o torniquete, y en un tanto similar la maniobra de
      Kristeller. La episiotomía se aplicó en tres cuartos de los casos.
    </p>
    <p>
      Las entrevistadas también pusieron en evidencia que la falta de
      consentimiento y los malos tratos son comunes. Casi la mitad dijo que el
      personal médico actuó sin pedir su consentimiento, lo que viola los
      derechos humanos de las pacientes, según la
      <a
        href="https://saludmentalperinatal.es/wp-content/uploads/2019/09/A_74_137-ES.pdf"
        >Relatora Especial sobre la violencia contra la mujer de las Naciones
        Unidas (ONU)</a
      >.
    </p>
    <p>
      Además, en el 41% de los casos las madres dijeron haber sufrido violencia
      verbal o psicológica. El personal médico las ignoraba cuando hacían alguna
      petición o las acusaban de poner en riesgo la vida de sus bebés.
    </p>
    <p class="destacado-doc">
      Cuba no es el único país donde aún son comunes estas y otras prácticas
      médicas que violentan a las mujeres. Se trata de un fenómeno global,
      relacionado con el machismo y la cultura patriarcal que atraviesa los
      sistemas de salud, y que ha sido visibilizado por el feminismo.
    </p>
    <p>
      Según Eva Margarita García, doctora en Antropología y autora de la
      <a
        href="https://repositorio.uam.es/bitstream/handle/10486/684184/garcia_garcia_eva_margarita.pdf"
      >
        primera tesis sobre violencia obstétrica en Europa </a
      >, la violencia obstétrica es la suma de violencia de género y mala praxis
      médica. Ella la define como aquella violencia ejercida por parte del
      personal sanitario sobre los cuerpos de las mujeres y su vida reproductiva
      mediante un trato deshumanizado, un abuso de medicalización y una
      patologización de los procesos fisiológicos.
    </p>
    <p>
      Para la experta, esta violencia es mediada por un sesgo de género que
      infantiliza a las mujeres como excusa para tratarlas de modo vejatorio. No
      obstante, se trata de una práctica tan normalizada socialmente que resulta
      difícil identificarla como un problema.
    </p>
    <p>
      Sin embargo, en Cuba se dan circunstancias que contribuyen a que este
      problema sea especialmente agudo. De acuerdo con profesionales de la salud
      entrevistados para esta investigación, el sistema de salud cubano es una
      organización vertical en la que los médicos apenas disponen de margen para
      introducir reformas.
    </p>
    <p>
      Reciben fuertes presiones para mantener ciertos indicadores estadísticos,
      sobre todo con respecto a mortalidad infantil, y tienen pocos incentivos
      para mejorar la calidad de la atención o pensar en el bienestar de las
      madres. Además, en un país que se considera una potencia médica y es
      gobernado de manera autoritaria, las posibilidades de reconocer y atender
      el problema son más reducidas que en otros países.
    </p>
    <img src="@/assets/img/reportajes/02-ILUSTRACION-WEB.jpg" alt="" />
    <h2>Estereotipos machistas</h2>
    <p>
      Para este reportaje se entrevistó a ocho médicos especialistas —cuatro
      mujeres y cuatro hombres—que forman o han formado parte del Programa de
      Atención Materno Infantil (PAMI), programa que centraliza la salud
      reproductiva de las mujeres en el país. De ellos, seis están en activo.
      Todos prefirieron mantener el anonimato por miedo a represalias como
      perder su puesto de trabajo o ser expulsados del Ministerio de Salud
      Pública (Minsap).
    </p>
    <p>
      Varios señalaron que en Cuba persisten estereotipos de género en el
      sistema médico que influyen sobre el trato que reciben las mujeres en el
      parto. Por ejemplo, un médico general integral con décadas de experiencia
      en la zona central del país justificó varias prácticas de violencia
      obstétrica “sobre todo en mujeres con trabajo de parto demorado o en
      mujeres que son ñoñas o majaderas”.
    </p>
    <p class="destacado-doc">
      También se observa una tendencia a ver a las mujeres en el parto como
      personas ignorantes y/o que deben ser pasivas. Informar, pedirles
      consentimiento, permitir acompañamiento o simplemente caminar durante el
      trabajo de parto es visto como un obstáculo para la labor de los
      profesionales. Como muestran las entrevistadas, no son prácticas comunes.
      “Se prioriza el bebé, la atención al recién nacido y que la mamá no
      sangre, pero olvidan al ser psicosocial”, relata un residente de
      ginecología y obstetricia de Holguín.
    </p>
    <p>
      También sucede que algunos obstetras creen que el parto siempre es
      doloroso y aliviar el sufrimiento no es prioritario. Además, a las
      pacientes que solicitan cesárea se les ve como buscando “comodidad” y se
      opta por “forzarlas” al parto vaginal.
    </p>
    <p>
      La expectativa de que las mujeres deben obedecer indicaciones médicas sin
      protestar es común en el personal de salud. Esta idea está tan arraigada
      que las propias mujeres han aprendido a recomendarse entre sí que es mejor
      “colaborar” o “portarse bien” —expresiones mencionadas de manera
      recurrente en los cuestionarios— para evitar empeorar el maltrato.
    </p>
    <p>
      Para Sandra Heidl, psicóloga y activista feminista que dio a luz en Cuba
      cuando tenía 19 años, en el sistema de salud pública cubano “el producto,
      el feto, es lo más importante” y la mujer pasa a un segundo plano como
      recipiente del producto. “Las mujeres aceptan o no son conscientes de
      estas violencias porque desean lo mejor para el hijo que está por nacer, y
      les han contado que los médicos tienen que decidir por el bien de sus
      bebés”, apunta Heidl.
    </p>
    <p>
      Esta subordinación de paciente a médico es uno de los rasgos de lo que se
      conoce como Modelo Médico Hegemónico (MMH). La doctora Daylis García
      Jordá, autora de
      <a
        href="http://tesis.sld.cu/FileStorage/000279-68B0-TESIS__Garc%C3%ADa_Jord%C3%A1.pdf"
        >uno de los pocos estudios</a
      >
      sobre violencia obstétrica en Cuba, considera que el MMH tiende a ver al
      paciente como ignorante o portador de ideas equivocadas, mientras que el
      conocimiento reside únicamente en el médico. A pesar de las críticas que
      se le han planteado, comenta García Jordá, este modelo continúa vigente y
      genera una experiencia de parto en la que lo que importa es el médico, no
      la paciente.
    </p>
    <p>
      Los sistemas de salud en muchos países están diseñados en función de las
      necesidades de los médicos, según comentaron para esta investigación los
      doctores Matthias Sachsee, especialista alemán en calidad de atención a la
      salud con experiencia en México, y Thaís Brandao, investigadora brasileña
      de salud sexual y reproductiva.
    </p>
    <p>
      Profesionales de la medicina cubana comentaron que algunas prácticas
      violentas se realizan por comodidad de los doctores, como el uso
      indiscriminado de la episiotomía. “Es la forma fácil de hacer el parto
      para el médico, para acabar rápido porque quieren terminar”, comenta un
      residente de Ginecología y Obstetricia de Holguín.
    </p>
    <p>
      Otras prácticas comunes como impedir caminar a la gestante, estar
      acompañada, realizar enemas o administrar antibióticos de manera
      preventiva también están relacionadas con las necesidades del sistema o
      las preferencias de los médicos, pero no con las necesidades de las
      mujeres.
    </p>
    <p>
      Por todo ello, la investigadora Brandao insiste en que la violencia
      obstétrica tiene “raíces institucionales” y que su causa principal es la
      falta voluntad del sistema de salud de atender el problema. En su opinión,
      la violencia obstétrica no está relacionada con la carencia de recursos.
    </p>
    <p>
      “Se pueden promover partos saludables, no violentos aún sin recursos,
      porque entiendes (como gobierno o sistema) que eso es lo importante”,
      comenta Brandao.
    </p>
    <h2>Un parto único</h2>
    <p>
      Desde 1975, casi el 100% de los nacimientos en Cuba ocurre en los
      hospitales públicos. A diferencia de las gestantes de otros países, las
      cubanas carecen de elección sobre dónde o cómo parir. Deben hacerlo en el
      único sistema existente controlado por el Minsap. Así, las normas,
      prioridades y carencias del Minsap moldean la experiencia de parir en
      Cuba.
    </p>
    <p>
      La institución ha mostrado que su principal objetivo es mantener bajos
      ciertos indicadores, en especial la mortalidad infantil: el número de
      niños que muere durante o poco después del parto.
    </p>
    <p>
      Esta es la tasa que cada año las autoridades presentan con orgullo como
      indicador de éxito de su sistema de atención al parto. “Es la estadística
      mejor cuidada del ministerio”, aseguró uno de los médicos entrevistados.
    </p>
    <p>
      “En Cuba el sistema está estructurado de forma que responde más a
      parámetros numéricos y funciona en respuesta a las necesidades de los
      profesionales o de la Salud Pública como entidad, y no de las mujeres y
      sus familias en el momento en que traen una nueva vida a este mundo”,
      <a
        href="http://tesis.sld.cu/FileStorage/000279-68B0-TESIS__Garc%C3%ADa_Jord%C3%A1.pdf"
        >precisa</a
      >
      la académica García Jordá en su estudio.
    </p>
    <img src="@/assets/img/reportajes/03-POSPARTO-WEB.jpg" alt="" />
    <p>
      Los profesionales consultados coincidieron en que reciben presión del
      Minsap para lograr buenas estadísticas. Deben siempre cumplir estrictos
      protocolos, lo que les desincentiva a introducir cambios o actuar bajo su
      propio criterio. También es común que deban cumplir cuotas, por ejemplo,
      sobre el número máximo de cesáreas que pueden practicar.
    </p>
    <p>
      Muchos médicos critican la presión a las que se les somete y algunos
      sienten que la rigidez de los protocolos les convierte en meros ejecutores
      de políticas diseñadas por burócratas que no conocen la realidad en la que
      trabajan.
    </p>
    <p class="destacadi-doc">
      “No puede ser que un programa donde interviene la gestión humana esté
      basado en índices y parámetros a cumplir. El médico no puede estar
      pensando en números, ni en cifras, ni en emulaciones porque es la vida de
      una paciente. Entonces, se trabaja bajo mucha presión”, comenta una
      obstetra, jubilada, con más de 20 años de experiencia.
    </p>
    <p>
      Una doctora recién graduada coincide. “Para mí, (la ginecobstetricia) es
      una de las especialidades donde más cuidados se tiene que tener, porque se
      lleva cabezas al momento por cualquier cosa”.
    </p>
    <p>
      Para las autoridades, el sistema funciona puesto que logran las
      estadísticas que buscan. En Cuba mueren menos
      <a href="https://data.worldbank.org/indicator/SH.STA.MMRT?view=map"
        >madres</a
      >
      y
      <a href="https://data.worldbank.org/indicator/SH.DYN.MORT.MA?view=map"
        >bebés</a
      >
      que en la mayoría de países de la región, lo que le permite al gobierno
      presumir de su sistema. La mortalidad infantil
      <a
        href="https://files.sld.cu/bvscuba/files/2021/08/Anuario-Estadistico-Espa%c3%b1ol-2020-Definitivo.pdf"
        >es muy similar</a
      >
      a la de países como
      <a
        href="https://www.cdc.gov/reproductivehealth/maternalinfanthealth/infantmortality.htm"
        >Estados Unidos</a
      >
      y la
      <a
        href="https://files.sld.cu/bvscuba/files/2021/08/Anuario-Estadistico-Espa%c3%b1ol-2020-Definitivo.pdf"
        >materna</a
      >, si bien es
      <a
        href="https://www.cdc.gov/nchs/data/hestat/maternal-mortality/2020/maternal-mortality-rates-2020.htm"
        >mucho más elevada</a
      >
      que la de los países del norte global, está entre las
      <a href="https://data.worldbank.org/indicator/SH.STA.MMRT?view=map"
        >más bajas</a
      >
      de Latinoamérica.
    </p>
    <p>
      Sin embargo, ninguna estadística da cuenta de la violencia obstétrica y la
      ausencia de parto humanizado. Pese a la abundancia de protocolos del
      Minsap, los profesionales consultados coincidieron en que los principios
      del parto humanizado, que empiezan a aplicarse en algunos países, son poco
      conocidos en Cuba.
    </p>
    <p>
      “Si te remites a bibliografía internacional lo conoces, pero no hay una
      parte del curso práctico que te hablen de esto. No es un tema que se
      debata siquiera”, afirma un residente de ginecología y obstetricia de
      Holguín.
    </p>
    <p>
      La Organización Mundial de la Salud (OMS) estableció en 2018 una serie de
      <a
        href="https://apps.who.int/iris/bitstream/handle/10665/272435/WHO-RHR-18.12-spa.pdf"
        >recomendaciones para conducir el trabajo de parto</a
      >. Lo primero que se reconoce es que el parto no puede estar sometido a
      estrictos protocolos que se apliquen por sistema, como sucede en Cuba,
      sino que debe basarse en el estado de la mujer y el bebé, “sus deseos y
      preferencias, y el respeto por su dignidad y autonomía”.
    </p>
    <p>
      La OMS recomienda alentar a las embarazadas a que se muevan y den a luz
      erguidas. También sugiere que se les permita estar acompañadas e incluso
      que puedan comer o beber durante el trabajo de parto; no separar a los
      bebés de las madres justo después del nacimiento; no aplicar técnicas que
      aceleren artificialmente los tiempos; limitar los tactos vaginales a uno
      cada cuatro horas; y no practicar la episiotomía si no es estrictamente
      necesaria.
    </p>
    <p>
      Estas recomendaciones no solo son respetuosas de los derechos de las
      mujeres sino también positivas desde el punto de vista médico. Múltiples
      estudios han mostrado que mientras más cómodas y acompañadas se sientan
      las gestantes, mayor es la probabilidad de que el parto vaginal sea
      exitoso y no se deba recurrir a técnicas más agresivas.
    </p>
    <p>
      Pero en Cuba, los cuestionarios y las entrevistas a profesionales ponen en
      evidencia que estas recomendaciones se ignoran.
    </p>
    <h2>Nada cálido o humano</h2>
    <p>
      Muchas mujeres describieron haber dado a luz en un ambiente carente de
      empatía, calidez o trato humano. Otras directamente dijeron haber recibido
      malos tratos, coacciones, y violencia verbal. Los profesionales del Minsap
      les privaron del momento que deseaban tener al nacer sus hijos. Esto
      contribuyó a que el parto se convirtiera en una fuente de trauma.
    </p>
    <p>
      Muchas entrevistadas afirmaron haber experimentado secuelas psicológicas
      tras su parto. En el 30% de los partos las mujeres tuvieron miedo a quedar
      nuevamente embarazadas, o manifestaron tener imágenes repetitivas de
      momentos del parto. En uno de cada cuatro partos, las mujeres dijeron que
      sufrieron cambios de humor, dificultades para dormir o miedo a enfrentarse
      al sistema de salud.
    </p>
    <p>
      Además, en el 14% de los partos las mujeres dijeron haber sufrido
      depresión posparto.
    </p>
    <p>
      “Casi nunca verás reflejadas (estas secuelas) como diagnóstico en las
      historias clínicas —explica uno de los profesionales consultados. “Estas
      pacientes casi nunca son derivadas a los servicios de salud mental para su
      tratamiento, lo cual termina afectando la salud física y la calidad de
      vida de las pacientes y las familias”, agregó.
    </p>
    <img src="@/assets/img/reportajes/04-JUSTICIA-WEB.jpg" alt="" />

    <p>
      La violencia verbal o psicológica, la falta de empatía o el sentimiento de
      abandono que describieron las mujeres en los cuestionarios tienen causas
      profundas, relacionadas con la cultura machista y el Modelo Médico
      Hegemónico. La verticalidad del sistema de salud cubano empeora ese
      contexto, según los médicos consultados.
    </p>
    <p class="destacado-doc">
      Varios profesionales admitieron que muchas veces trasladan a las mujeres
      las presiones y las carencias que sufren, algo que se ha intensificado a
      medida que los servicios médicos del país se han ido degradando por la
      falta de personal y recursos.
    </p>
    <p>
      En la actualidad, el personal médico en Cuba gana, al cambio oficial,
      entre 190 y 320 dólares al mes. Para sobrevivir, algunos de ellos aceptan
      regalos o dinero de los pacientes y normalmente reservan el mejor trato y
      los pocos materiales disponibles para “sus” casos.
    </p>
    <p>
      “La ginecobstetricia es una de las especialidades que más vive de eso. Si
      no tienes tu médico y no vas por la canaleta, como se dice en la calle,
      estás muy jodido”, comenta una doctora recién graduada a partir de su
      propia experiencia como madre.
    </p>
    <p>
      A pesar de la abundancia de profesionales sanitarios en el país, los
      entrevistados describieron horarios y turnos de trabajo cada vez más
      intensos por la falta de personal. Tras turnos de tres o cuatro días
      consecutivos de guardia, es común que el personal del PAMI tenga que
      trabajar en centros de salud o realizar visitas a domicilio.
    </p>
    <p>
      Al mismo tiempo, las exigencias de cumplir con los objetivos estadísticos
      no aflojan. “Tenemos que exhibir resultados a la altura de países que
      tienen servicios con todas las condiciones”, dice un médico entrevistado
      para
      <a href="http://tesis.sld.cu/index.php?ID=670&P=FullRecord">la tesis</a>
      de Lareisy Borges Damas, una doctora en Enfermería, quien ha investigado
      los modelos de parto humanizado.
    </p>
    <p>
      Con este panorama, varios profesionales aseguraron haber perdido la
      motivación, lo que está perjudicando la calidad de la atención que
      prestan.
    </p>
    <p>
      En palabras de un residente de ginecología y obstetricia de Holguín,
      “nadie quiere trabajar aquí. Entonces les dejan pasar esas violencias
      mientras no afecten las estadísticas. Puede haber maltrato mientras no
      muera la gestante o el bebé. Más o menos así funciona”.
    </p>
